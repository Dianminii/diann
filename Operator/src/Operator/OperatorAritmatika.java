package Operator;

/**
 *
 * @author ASUS
 */
public class OperatorAritmatika {
  public static void main(String[] args) {
        System.out.println("Operasi Penjumlahan");
        int hasilPenjumlahan = 1 + 9;
        System.out.println("Hasil 1 + 9 = " + hasilPenjumlahan);
        System.out.println();
        System.out.println("Operasi Pengurangan");
        int hasilPengurangan = 6 - 4;
        System.out.println("Hasil 6 - 4 = " + hasilPengurangan);
        System.out.println();
        System.out.println("Operasi Pengalian");
        int hasilPengalian = 3 * 7;
        System.out.println("Hasil 3 * 7 = " + hasilPengalian);
        System.out.println();
        System.out.println("Operasi Pembagian");
        int hasilPembagian = 20 / 4;
        System.out.println("Hasil 20 / 4 = " + hasilPembagian);
        System.out.println();
        System.out.println("Operasi Habis bagi");
        int hasilSisa = 9 % 3;
        System.out.println("Hasil 9 % 3 = " + hasilSisa);
        int hasilSisaLain = 8 % 3;
        System.out.println("Hasil 8 % 3 = " + hasilSisaLain);
        System.out.println();
    }  
}
